void getNESPalette(QRgb* palette, size_t size)
{
	sukiAssert(size > 0 && size <= 64);
	sukiAssert(palette);

	for(uint32 i = 0; i<size; ++i)
	{
		byte r = ntscpalette_pal[(i * 3)];
		byte g = ntscpalette_pal[(i * 3) + 1];
		byte b = ntscpalette_pal[(i * 3) + 2];

		palette[i] = qRgb(r, g, b);
	}
}